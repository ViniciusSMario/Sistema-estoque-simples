    public void preencher_tabela(Vendas_model v) {
        try{
            modelo = (DefaultTableModel) jTable1.getModel();
            modelo.setRowCount(0);

            Vendas_controller pc = new Vendas_controller();
            list_venda = pc.buscar(v); // busca e retorno

            for (int i=0; i<list_venda.size();i++) {
              modelo.addRow(new Object[]{
                       list_venda.get(i).getIdvenda(),
                       list_venda.get(i).getData(),
                       list_venda.get(i).getTotalpagar(),
                       total = total + list_venda.get(i).getTotalpagar()
                     });
                }
            
            tfVTGasto.setText(String.valueOf(total));
        }catch(Exception e){
             JOptionPane.showMessageDialog(null, "Erro: " + e.getMessage(), "Alerta", 2);
         }
    }
