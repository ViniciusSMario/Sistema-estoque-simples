     public void busca() {

        Clientes_controller t = new Clientes_controller();
      
        List<Clientes_model> lista_cli = (List<Clientes_model>) t.listar_dados();
        
        for(int i=0; i<lista_cli.size();i++)
        {
            cbCliente.addItem(lista_cli.get(i).getNomeCliente());
            
        }

    }
     
     public int busca2()
    {
        Clientes_controller t = new Clientes_controller();
        List<Clientes_model> lista_cliente = (List<Clientes_model>) t.listar_dados();

        for(int i=0; i<lista_cliente.size();i++)
        {
            if(cbCliente.getSelectedItem().toString().equals(lista_cliente.get(i).getNomeCliente()))
            {
                id_cli = lista_cliente.get(i).getIdCliente();
            }
        }
        return id_cli;
    }
     
   
