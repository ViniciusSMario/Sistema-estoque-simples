     public int busca4()
    {
 
        Produtos_controller t = new Produtos_controller();

        List<Produtos_model> lista_produto = (List<Produtos_model>) t.listar_dados();
        
        for(int i=0; i<lista_produto.size();i++)
        {
            if(cbProduto.getSelectedItem().toString().equals(lista_produto.get(i).getNomeProduto()))
            {
                id = lista_produto.get(i).getIdProduto();
                totalparc = Integer.parseInt(tfQtde.getText())* lista_produto.get(i).getValor_venda();
                totalpag = totalpag + totalparc;
                tfVT.setText(String.valueOf(totalpag));
                
                quant = lista_produto.get(i).getQauntidade();
                
                item_prod it = new item_prod();

                it.setDescProduto(lista_produto.get(i).getNomeProduto());
                it.setPreco(lista_produto.get(i).getValor_venda());
                it.setQtde(Integer.parseInt(tfQtde.getText()));
                it.setTotal(totalparc);
                list_pro.add(it);
            }
        }
        return id;

    }
     
     
  public int busca5()
    {
        Clientes_controller t = new Clientes_controller();
        List<Clientes_model> lista_cliente = (List<Clientes_model>) t.listar_dados();

        for(int i=0; i<lista_cliente.size();i++)
        {
            if(cbCliente.getSelectedItem().toString().equals(lista_cliente.get(i).getNomeCliente()))
            {
                id_cli = lista_cliente.get(i).getIdCliente();
            }
        }
        return id_cli;
    }
  
    public void preencher_tabela() {
         DefaultTableModel modelo = (DefaultTableModel) jTable1.getModel();
         modelo.setRowCount(0);

         for(int a=0; a<list_pro.size();a++){
            modelo.addRow(new Object[]{
              list_pro.get(a).getDescProduto(),
              list_pro.get(a).getQtde(),
              list_pro.get(a).getPreco(),
              list_pro.get(a).getTotal()
         });
         }
    }
