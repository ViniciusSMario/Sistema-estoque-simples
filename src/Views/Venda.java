    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        Produtos_model p1 = new Produtos_model();
        Vendas_model v = new Vendas_model();
        ItemVendas_model iv = new ItemVendas_model();
        
        if(x==1){ // primeira vez salva depois só altera
            v.setIdvenda(cod);
            v.setIdCliente(busca5());
            v.setTotalpagar(totalpag);

        try { 
            DateFormat formatador = new SimpleDateFormat("dd/MM/yyyy");
            v.setData(new java.sql.Date(((java.util.Date) formatador.parse(jFormattedTextField1.getText())).getTime()));
           } catch (ParseException ex) {
               Logger.getLogger(Venda.class.getName()).log(Level.SEVERE, null, ex);
           }

            Vendas_controller ven = new Vendas_controller();
            ven.salvar(v);
        }
        x++;
       
       
        iv.setIdProduto(busca4()); // chama o metodo para pegar o idproduto para salvar na tabela itemvenda
        iv.setIdVenda(Integer.parseInt(tfCod.getText()));
        iv.setQtde(Integer.parseInt(tfQtde.getText()));
        iv.setTotalparc(totalparc);
          
        p1.setIdProduto(iv.getIdProduto());
        qt = quant - iv.getQtde();
        
        if(qt < 0){
           JOptionPane.showMessageDialog(null, "Não há estoque suficiente!!!!", "Vendas", 1);
           cbProduto.setSelectedIndex(0);
           tfVT.setText("");
           
           dispose();
           Menu x = new Menu();
           x.setVisible(true);
          
        }else if(qt <= 8 && qt > 0){
            p1.setQauntidade(qt);
            
            ItemVendas_controller item = new ItemVendas_controller();
            item.salvar(iv);
            
            Produtos_controller p = new Produtos_controller();
            p.alterar(p1);
            
            preencher_tabela();
            
            JOptionPane.showMessageDialog(null, "Estoque Baixo! Só um aviso!!", "Aviso Estoque", 1);

        }else{
            p1.setQauntidade(qt);

            ItemVendas_controller item = new ItemVendas_controller();
            item.salvar(iv);
            
            Produtos_controller p = new Produtos_controller();
            p.alterar(p1);
            
            preencher_tabela();
        }        
    }//GEN-LAST:event_jButton1ActionPerformed

